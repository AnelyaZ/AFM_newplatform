function esc(s: string) { return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;'); }
function p(str: string) { if (!str.trim()) return '<p><br/></p>'; return `<p>${esc(str)}</p>`; }
function ul(items: string[]) { return `<ul>${items.map((it) => `<li>${esc(it)}</li>`).join('')}</ul>`; }

function sliceByMarkers(src: string) {
  const idxPlacement = src.indexOf('Стадия размещения');
  const idxLayer = src.indexOf('Стадия расслоения');
  const idxIntegration = src.indexOf('Стадия интеграции');
  const intro = src.substring(0, idxPlacement > -1 ? idxPlacement : src.length).trim();
  const placement = idxPlacement > -1 ? src.substring(idxPlacement, idxLayer > -1 ? idxLayer : src.length).trim() : '';
  const layering = idxLayer > -1 ? src.substring(idxLayer, idxIntegration > -1 ? idxIntegration : src.length).trim() : '';
  const integration = idxIntegration > -1 ? src.substring(idxIntegration).trim() : '';
  return { intro, placement, layering, integration };
}

function buildHtmlIntro(text: string) {
  const lines = text.split('\n');
  return `<div>${lines.map((l) => p(l)).join('')}</div>`;
}

function buildHtmlPlacement(text: string) {
  const lines = text.split('\n').map((l) => l.trim());
  return `<div>${lines.map((l) => p(l)).join('')}</div>`;
}

function buildHtmlLayering(text: string) {
  const lines = text.split('\n').map((l) => l.trim());
  // Collect bullets starting with "- "
  const bulletStart = lines.findIndex((l) => l.startsWith('- '));
  const headPart = bulletStart >= 0 ? lines.slice(0, bulletStart) : lines;
  const bullets = bulletStart >= 0 ? lines.slice(bulletStart).filter((l) => l.startsWith('- ')).map((l) => l.replace(/^\-\s*/, '')) : [];
  const tail = bulletStart >= 0 ? lines.slice(bulletStart + bullets.length) : [];
  return `<div>${headPart.map((l) => p(l)).join('')}${bullets.length ? ul(bullets) : ''}${tail.map((l) => p(l)).join('')}</div>`;
}

function buildHtmlIntegration(text: string) {
  const lines = text.split('\n');
  return `<div>${lines.map((l) => p(l)).join('')}</div>`;
}

async function main() {
  const prisma = new PrismaClient();
  try {
    const course = await prisma.course.findFirst({ where: { title: { equals: 'Базовый курс АФМ' } }, include: { chapters: { orderBy: { orderIndex: 'asc' } } } });
    if (!course) throw new Error('Course "Базовый курс АФМ" not found');
    const chapters = course.chapters || [];
    // Stage 4
    let chapter = chapters.find((c: { title: string }) => /Этап\s*№?4/i.test(c.title));
    if (!chapter) chapter = chapters.find((c: { orderIndex: number }) => c.orderIndex === 7) || chapters[6];
    if (!chapter) throw new Error('Stage 4 chapter not found');

    // Clean existing lessons
    const existing = await prisma.lesson.findMany({ where: { chapterId: chapter.id }, select: { id: true } });
    if (existing.length) {
      await prisma.lessonContent.deleteMany({ where: { lessonId: { in: existing.map((x: { id: string }) => x.id) } } });
      await prisma.lesson.deleteMany({ where: { id: { in: existing.map((x: { id: string }) => x.id) } } });
    }

    const slices = sliceByMarkers(SRC);
    const lessons = [
      { title: 'Введение', html: buildHtmlIntro(slices.intro) },
      { title: 'Стадия размещения', html: buildHtmlPlacement(slices.placement) },
      { title: 'Стадия расслоения', html: buildHtmlLayering(slices.layering) },
      { title: 'Стадия интеграции', html: buildHtmlIntegration(slices.integration) },
    ];

    let order = 1;
    for (const l of lessons) {
      const created = await prisma.lesson.create({ data: { chapterId: chapter.id, orderIndex: order++, title: l.title, description: null } });
      await prisma.lessonContent.create({ data: { lessonId: created.id, blockType: 'TEXT', textHtml: l.html, sortIndex: 1 } });
    }
    console.log(`Created ${lessons.length} lessons under chapter: ${chapter.title}`);
  } finally {
    await (prisma as any).$disconnect();
  }
}

main().catch((e) => { console.error(e); process.exit(1); });


